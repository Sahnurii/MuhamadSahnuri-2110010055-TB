    //Coding Cari Data
    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
       try{
           java.sql.Connection conn=(java.sql.Connection)gui.koneksi.koneksiDB();
           String sql = "SELECT * FROM barang WHERE kode_barang='"+tfCari.getText()+"' or nama='"+tfCari.getText()+"'";
           java.sql.PreparedStatement pst = conn.prepareStatement(sql);
           ResultSet rs = pst.executeQuery(sql);
           
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           model.setRowCount(0);
           
           // mengisi tabel sesuai hasil pencarian
           while (rs.next()){
               model.addRow(new Object[]{
                rs.getString("kode_barang"),
                rs.getString("nama"),
                rs.getString("kategori"),
                rs.getString("jumlah"),
                rs.getString("keadaan")
                       
            });
            
               
        }
           
       //Menampilkan Pesan Error Ketika Cari data gagal   
       }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Pencarian Data Gagal");
            System.out.println(e.getMessage());
            tfCari.requestFocus();
       }
    }//GEN-LAST:event_btnCariActionPerformed
    
