    private void loadCategoryNames() {
    try {
        Connection c = koneksi.koneksiDB();
        Statement s = c.createStatement();

        String sql = "SELECT nama_kategori FROM tb_kategori";
        ResultSet r = s.executeQuery(sql);

        while (r.next()) {
            cbKategori.addItem(r.getString("nama_kategori"));
        }

        r.close();
        s.close();
    } catch (Exception e) {
        System.out.println("Error loading category names: " + e.getMessage());
    }
}
    
    

            
